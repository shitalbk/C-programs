public class inv {
    private String invItemname;
    private int invQuantity;
    private int invPrice;
    
    public void setItemname(String invItemname)
    {
        this.invItemname = invItemname;
    }
    public String getItemname()
    {
        return invItemname;
    }
    public void setquantity(int invQuantity)
    {
        this.invQuantity = invQuantity;
    }
    public int getquantity()
    {
        return invQuantity;
    }
    public void setprice(int invPrice)
    {
        this.invPrice = invPrice;
    }
    public int getprice()
    {
        return invPrice;
    }
    
}
